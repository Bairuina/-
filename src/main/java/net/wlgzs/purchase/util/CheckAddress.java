    public static String checkOneAddressWordByNumber(String dataNumber){

        for (String dataCheck:allAddressData){
            if(dataCheck.startsWith(dataNumber+"	")){
                return dataCheck.split("	")[1];
            }
        }
        return "无查询结果";
    }

    public static String checkOneAddressNumberByWord(String dataWord){
        for (String dataCheck:allAddressData){
            if(dataCheck.endsWith("	"+dataWord)){
                return dataCheck.split("	")[0];
            }
        }
        return "无查询结果";
    }

